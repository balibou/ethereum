import React, { Component } from 'react'
import { connect } from 'react-redux'
import SimpleStorageContract from '../build/contracts/SimpleStorage.json'
import getWeb3 from './utils/getWeb3'

import './css/oswald.css'
import './css/open-sans.css'
import './css/pure-min.css'
import './App.css'

class App extends Component {
  constructor(props) {
    super(props)

    this.state = {
      storageValue: 0,
      web3: null
    }
  }

  componentWillMount() {
    // Get network provider and web3 instance.
    // See utils/getWeb3 for more info.

    getWeb3
    .then(results => {
      this.setState({
        web3: results.web3
      })

      // Instantiate contract once web3 provided.
      this.instantiateContract()
    })
    .catch(() => {
      console.log('Error finding web3.')
    })
  }

  instantiateContract() {
    /*
     * SMART CONTRACT EXAMPLE
     *
     * Normally these functions would be called in the context of a
     * state management library, but for convenience I've placed them here.
     */

    const contract = require('truffle-contract')
    const simpleStorage = contract(SimpleStorageContract)
    simpleStorage.setProvider(this.state.web3.currentProvider)

    // Declaring this for later so we can chain functions on SimpleStorage.
    var simpleStorageInstance

    // Get accounts.
    this.state.web3.eth.getAccounts((error, accounts) => {
      simpleStorage.deployed().then((instance) => {
        simpleStorageInstance = instance

        // Stores a given value, 5 by default.
        // return simpleStorageInstance.set(5, {from: accounts[0]})
      }).then((result) => {
        this.setState({ firstAccount: accounts[0] })
        // Get the value from the contract to prove it worked.
        return simpleStorageInstance.get.call(accounts[0])
      }).then((result) => {
        // Update state with the result.
        return this.setState({ storageValue: result.c[0] })
      }).catch((err) => {
        return this.setState({ error: 'wrong network' })
      })
    })
  }

  render() {
    if (this.state.error){
      return (
        <div className="App">
        <nav className="navbar pure-menu pure-menu-horizontal">
            <a href="#" className="pure-menu-heading pure-menu-link">Truffle Box</a>
        </nav>

        <main className="container">
          <div className="pure-g">
            <div className="pure-u-1-1">
              <p>Oops, youâ€™re on the wrong network</p>
              <p>Simply open MetaMask and switch over to the Main Ethereum Network.</p>
            </div>
          </div>
        </main>
      </div>
      )
    }
    return (
      <div className="App">
        <nav className="navbar pure-menu pure-menu-horizontal">
            <a href="#" className="pure-menu-heading pure-menu-link">Truffle Box</a>
        </nav>

        <main className="container">
          <div className="pure-g">
            <div className="pure-u-1-1">
              <h1>Good to Go!</h1>
              <p>Your Truffle Box is installed and ready.</p>
              <h2>Smart Contract Example</h2>
              <p>If your contracts compiled and migrated successfully, below will show a stored value of 5 (by default).</p>
              <p>Try changing the value stored on <strong>line 59</strong> of App.js.</p>
              <p>The stored value is: {this.state.storageValue}</p>

              {
                this.state.firstAccount === '0xb185e019f3164386c9d8c94bcb734e70010b7e2e'
                ? <div>
                  <p>You have an account on our platform !</p>
                </div>
                : <div>
                  {/*Good network but no account*/}
                  {/*Associate a mail and a nickname to that account*/}
                  <p>Wallet address:</p>
                  <p>{this.state.firstAccount}</p>
                </div>
              }
            </div>
          </div>
        </main>
      </div>
    );
  }
}

const mapStateToProps = state => ({
  count: state.counter.count
})

export default connect(
  mapStateToProps,
)(App)
